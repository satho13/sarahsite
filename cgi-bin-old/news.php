       	<b>8/2/2013:</b><br />
		<b><a href="http://kagan.seas.upenn.edu/publications.php?year=2013">Plasmonic Enhancement of Nanophosphor Upconversion</a></b>
	<br><br>
		Arrays of subwavelength holes (nanoholes) in Au films were computationally designed, fabricated and used as templates to localize and enhance the luminescence of upconversion nanophosphors. Enhancements of up to 35-fold and a concomitant shortening of the UCNP luminescence rise time were observed, consistent with simulations of plasmonic enhancement of the UCNP absorption.<br>
