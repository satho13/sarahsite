	<p></p>


       	<b>1/31/2013:</b><br />
		<b><a href="http://kagan.seas.upenn.edu/publications.php?year=2013">Nanocrystal Stoichiometric Imbalance Doping</a></b>
	<br><br>
		We manipulate the stoichiometry of Pb-chalcogenide nanocrystal arrays through thermal evaporation of Pb or Se to engineer their carrier statistics. Excess Pb forms NC FETs with electron mobilities of 10 cm2/Vs and excess Se form Schottky solar cells with enhanced power conversion efficiency.<br>
