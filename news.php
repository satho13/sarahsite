<b>1/15/2019:</b><br />
	<b><a href="publications.php?year=2019">Magnetic-field-dependent quantum emission in hexagonal boron nitride at room temperature</a></b>
	<br><br>
		Recently, the van der Waals material hexagonal boron nitride (h-BN) has emerged as a robust host for quantum emitters, promising efficient photon extraction and atom-scale engineering, but observations of spin-related effects have remained thus far elusive. Here, we report room-temperature observations of strongly anisotropic photoluminescence patterns as a function of applied magnetic field for select quantum emitters in h-BN.<br><br>
