		<b><a href="http://kagan.seas.upenn.edu/publications.php?year=2012">Enhancing PV Efficiency</a></b>
	<br><br>
		Exploiting wrinkles and deep folds that form on polymer surfaces when subjected to mechanical stress we show in Nature Photonics that light is guided and retained in the photoactive regions of photovoltaics to increase external quantum efficiency particularly in the NIR where light is otherwise minimally absorbed.	<br>
