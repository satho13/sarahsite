       	<b>9/1/2012:</b><br />
		<b><a href="http://kagan.seas.upenn.edu/publications.php?year=2012">Metal Enhanced Upconversion Luminescence</a></b>
	<br><br>
		We report luminescence enhancement of upconversion nanophosphors (UCNPs) that depends on the separation between the UCNPs and metal NPs with enhancement of up to 5.2-fold proximal to Au NPs and up to 45-fold proximal to Ag NPs arising from resonant plasmonic coupling and non-resonant near field-enhancement from the metal NPs.<br>
