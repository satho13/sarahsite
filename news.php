	<b><a href="publications.php?year=2019">Network architecture of energy landscapes in mesoscopic quantum systems</a></b>
	<br><br>
	Mesoscopic quantum systems exhibit complex many-body quantum phenomena, where interactions between spins and charges give rise to collective modes and topological states. Even simple, non-interacting theories display a rich landscape of energy states --- distinct many-particle configurations connected by spin- and energy-dependent transition rates. The collective energy landscape is difficult to characterize or predict, especially in regimes of frustration where many-body effects create a multiply degenerate landscape. Here we use network science to characterize the complex interconnection patterns of these energy-state transitions.
