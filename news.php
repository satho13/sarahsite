<b>11/2/2017:</b><br />
	<b><a href="publications.php?year=2019">Imaging a nitrogen-vacancy center with a diamond immersion metalens</a></b>
	<br><br>
	In this letter, we image an individual quantum emitter, an isolated nitrogen-vacancy (NV) center in diamond, using a dielectric metalens composed of subwavelength pillars etched into the diamond's surface. The metalens eliminates the need for an objective by operating as a high-transmission-efficiency immersion lens with a numerical aperture (NA) greater than 1.0. This design provides a scalable approach for fiber coupling solid-state quantum emitters that will enable the development of deployable quantum devices.
