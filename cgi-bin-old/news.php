       	<b>1/31/2014:</b><br />
		<b><a href="http://kagan.seas.upenn.edu/publications.php?year=2014">Air-Stable, Solution Processable Ag NC Materials</a></b>
	<br><br>
		We describe a room-temperature, chemical process to transform silver nanocrystal solids, deposited from colloidal solutions, into highly conductive, corrosion-resistant, optical and electronic materials with nanometer-scale architectures. We show ammonium thiocyanate uniquely form low-resistivity, optically smooth thin films that can be patterned by imprint lithography to form conductive electrodes and plasmonic mesostructures with programmable resonances. <br>
