       	<b>11/20/2012:</b><br />
		<b><a href="http://kagan.seas.upenn.edu/publications.php?year=2012">Flexible Nanocrystal Integrated Circuits</a></b>
	<br><br>
		We report solution-deposited nanocrystal integrated circuits (NCICs), showing NCIC inverters, amplifiers, and ring oscillators, constructed from high-performance, low-voltage, low-hysteresis CdSe nanocrystal field-effect transistors (FETs). We fabricated nanocrystal FETs and NCICs from colloidal inks on flexible plastic substrates that operate at low voltage.<br>
