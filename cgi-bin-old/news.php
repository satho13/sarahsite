       	<b>9/18/2013:</b><br />
		<b><a href="http://kagan.seas.upenn.edu/publications.php?year=2013">Solution-Based Stoichiometric Control over Charge Transport in Nanocrystalline CdSe Devices</a></b>
	<br><br>
		We demonstrate a mild, room-temperature, wet-chemical process to chemically manipulate the atomic composition of and thereby dope CdSe nanowires (NWs) in completed devices; where Cd enhances electron currents and chalcogens suppresses them. Subsequent thermal diffusion of the n-dopant indium depends on the surface composition, consistent with an indium-chalcogen interaction at the surface rather than in the NW core and with differences observed in CdSe nanocrystal devices.<br>
