       	<b>12/30/2013:</b><br />
		<b><a href="http://kagan.seas.upenn.edu/publications.php?year=2013">Solution-Processed Phase-Change VO2 Metamaterials</a></b>
	<br><br>
		We demonstrate thermally switchable VO2 metamaterials by combining nanoimprinting and solution-based deposition of colloidal nanocrystals. We tuned the phase change temperature by W-doping the NCs and fabricated differentially-doped, multilayered materials to tailor the optical response in the near-IR and IR from that of a strong light absorber to that of a Drude like reflector with a change in temperature.  <br>
