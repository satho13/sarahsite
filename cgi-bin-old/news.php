       	<b>8/16/2013:</b><br />
		<b><a href="http://kagan.seas.upenn.edu/publications.php?year=2013">In-situ Repair for Large-Area Fabrication and Air-stable Operation of Nanocrystal Electronics</a></b>
	<br><br>
		We introduce an in-situ chemical agent, namely indium metal that is thermally triggered to diffuse and repair trap states in semiconductor nanocrystals created upon air and solvent exposure. Exploiting the recovery, we show nanocrystal devices may be fabricated in air using conventional cleanroom technologies to realize large-area and flexible nanocrystal field-effect transistors that operate in air stably over months.<br>
