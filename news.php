       	<b>4/18/2012:</b><br />
		<b><a href="http://kagan.seas.upenn.edu/publications.php?year=2012" target="blank">Remote Doping in NW FETs</a></b>
	<br><br>
		In ACS Nano we show single, strongly quantum confined single crystalline PbSe nanowires behave as Schottky barrier FETs. Surface species allow for remote doping with no evidence of impurity scattering at low temperatures, providing a promising route to dope nanostructures.	<br>
