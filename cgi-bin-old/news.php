       	<b>12/10/2012:</b><br />
		<b><a href="http://kagan.seas.upenn.edu/publications.php?year=2012">Nanoimprinted Nanocrystal Metamaterials</a></b>
	<br><br>
		We design optical metamaterials using colloidal gold nanocrystal building blocks, tailoring the dielectric permitivity of the solid by exchanging the nanocrystals' surface-capping molecules. Direct, wide-area nanoimprinting of subwavelength superstructures affords plasmonic resonances with tunability ranging from 660 to 1070 nm.<br>
