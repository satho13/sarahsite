       	<b>2/6/2014:</b><br />
		<b><a href="http://kagan.seas.upenn.edu/publications.php?year=2014">High-Performance Lead Chalcogenide Devices through Post-synthesis, Colloidal Atomic Layer Deposition</a></b>
	<br><br>
		We report a simple, solution-based, post-synthetic colloidal, atomic layer deposition (PS-cALD) process to engineer stepwise the surface stoichiometry and therefore the electronic properties of lead chalcogenide nanocrystal (NC) thin films integrated in devices. We found that unlike chalcogen-enriched NC surfaces that are structurally, optically and electronically unstable, lead chloride treatment creates a well-passivated shell that stabilizes the NCs. Using PS-cALD of lead chalcogenide NC thin films we demonstrate high electron field-effect mobilities of ~4.5 cm2/Vs. <br>
